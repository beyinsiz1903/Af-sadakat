export const RTL_LANGUAGES = ['ar'];

export function isRtl(lang) {
  return RTL_LANGUAGES.includes(lang);
}

/**
 * translate(englishKey, lang, fallback?)
 * - If lang === 'en' returns the key as-is
 * - Otherwise looks up translations[lang][englishKey]
 * - Falls back to provided fallback then to the original key
 */
export function translate(key, lang, fallback) {
  if (!key) return key;
  if (lang === 'en') return key;
  return translations[lang]?.[key] || fallback || key;
}

export default translations;
